function genz_get_field_id_by_prefix($text, $prefix = '', $suffix = '')
{
    return 'genz_' . $prefix . str_replace('-', '_', sanitize_key($text)) . $suffix;
}

function genz_get_color_fields($type)
{

    $fields = [];
    $function_name = "genz_get_default_colors_{$type}";
    if (!function_exists($function_name)) return $fields;

    $colors = call_user_func($function_name);
    foreach ($colors as $color) {
        if (empty($color['id'])) continue;

        $color = wp_parse_args($color, [
            'id' => '',
            'title' => '',
            'value' => ''
        ]);


        if (empty($color['title'])) {
            $color['title'] = ucfirst(str_replace('-', ' ', $color['id']));
        }
        $field_id = genz_get_field_id_by_prefix($color['id'], $type . '_');
        $fields[] = [
            'name' => $color['title'],
            'id'   => esc_attr($field_id),
            'type' => 'color',
            'std' => $color['value'],
            'alpha' => true
        ];
    }
    return $fields;
}

function genz_get_number_fields($type)
{

    $fields = [];
    $function_name = "genz_get_default_{$type}";
    if (!function_exists($function_name)) return $fields;

    $numbers = call_user_func($function_name);
    foreach ($numbers as $number) {
        if (empty($number['id'])) continue;

        $number = wp_parse_args($number, [
            'id' => '',
            'title' => '',
            'value' => ''
        ]);


        if (empty($number['title'])) {
            $number['title'] = ucfirst(str_replace('-', ' ', $number['id']));
        }
        $field_id = genz_get_field_id_by_prefix($number['id']);
        $fields[] = [
            'name' => $number['title'],
            'id'   => esc_attr($field_id),
            'type' => 'number',
            'std' => $number['value'],
            'min' => 1,
            'max' => 100,
            'step' => 1,
            'suffix' => 'px',
            'desc' => 'in pixel'
        ];
    }
    return $fields;
}
